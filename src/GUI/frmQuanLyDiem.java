    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
         JFileChooser fileChooser = new JFileChooser();
    int result = fileChooser.showSaveDialog(this);
    
    if (result == JFileChooser.APPROVE_OPTION) {
        File file = fileChooser.getSelectedFile();
       
        try (BufferedWriter writer = new BufferedWriter(new FileWriter(file))) {
            // Ghi dữ liệu vào file
            // Ví dụ: Ghi dữ liệu từ bảng tblSinhVien vào file
            
            for (int row = 0; row < tblSinhVien.getRowCount(); row++) {
                for (int column = 0; column < tblSinhVien.getColumnCount(); column++) {
                    Object value = tblSinhVien.getValueAt(row, column);
                    writer.write(value.toString());
                    
                    if (column < tblSinhVien.getColumnCount() - 1) {
                        writer.write(", "); // Phân tách giữa các cột
                    }
                }
                
                writer.newLine(); // Xuống dòng sau mỗi dòng
            }
            
            JOptionPane.showMessageDialog(this, "FIle Luu thanh cong roi do.");
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Loi roi : " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        // Lấy thông tin từ các trường nhập liệu
    String maSV = txtMaSV.getText();
    String hoTen = txtHoTen.getText();
    float diemTA, diemTH, diemGDTC;
    try {
        diemTA = Float.parseFloat(txtTiengAnh.getText());
        diemTH = Float.parseFloat(txtTinHoc.getText());
        diemGDTC = Float.parseFloat(txtGDTC.getText());
    } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(this, "Điểm phải là một số.");
        return;
    }

    // Kiểm tra xem các trường nhập liệu có bị bỏ trống không
    if (maSV.isEmpty() || hoTen.isEmpty() || txtTiengAnh.getText().isEmpty() || txtTinHoc.getText().isEmpty() || txtGDTC.getText().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Vui lòng điền đầy đủ thông tin.");
        return;
    }

    // Gọi phương thức SuaSinhVien từ lớp DAO.tblStudents để cập nhật sinh viên trong CSDL
    int updateResult = DAO.tblStudents.SuaSinhVien(maSV, hoTen, diemTA, diemTH, diemGDTC);

    // Kiểm tra kết quả cập nhật sinh viên và hiển thị thông báo tương ứng
    if (updateResult > 0) {
        JOptionPane.showMessageDialog(this, "Cập nhật sinh viên thành công.");
        HienThiDS(); // Refresh the display of the student list
    } else if (updateResult == -1) {
        JOptionPane.showMessageDialog(this, "Lỗi kết nối cơ sở dữ liệu.");
    } else {
        JOptionPane.showMessageDialog(this, "Cập nhật sinh viên thất bại. Vui lòng kiểm tra lại thông tin.");
    }
        
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
            // Lấy từ khóa tìm kiếm từ trường nhập liệu
    String masv = txtSearch.getText();

    // Gọi phương thức TimSinhVienTheoMASV từ lớp DAO.tblStudents để tìm sinh viên theo MASV
    Students student = DAO.tblStudents.TimSinhVienTheoMASV(masv);

    // Kiểm tra kết quả tìm kiếm và cập nhật bảng hiển thị
    if (student != null) {
        DefaultTableModel model = (DefaultTableModel) tblSinhVien.getModel();
        model.setRowCount(0); // Xóa dữ liệu cũ trong bảng

        Object[] row = new Object[6];
        row[0] = student.getMasv();
        row[1] = student.getHoten();
        row[2] = student.getTiengAnh();
        row[3] = student.getTinHoc();
        row[4] = student.getGdtc();
        row[5] = student.tinhDiemTrungBinh() ;
        model.addRow(row);
    } else {
        JOptionPane.showMessageDialog(this, "Không tìm thấy sinh viên nào.");
    }

    }//GEN-LAST:event_btnSearchActionPerformed


    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
         // Lấy thông tin từ các trường nhập liệu
    String maSV = txtMaSV.getText();
    String hoTen = txtHoTen.getText();
    float diemTA, diemTH, diemGDTC;
    try {
        diemTA = Float.parseFloat(txtTiengAnh.getText());
        diemTH = Float.parseFloat(txtTinHoc.getText());
        diemGDTC = Float.parseFloat(txtGDTC.getText());
    } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(this, "Điểm phải là một số.");
        return;
    }

    // Kiểm tra xem các trường nhập liệu có bị bỏ trống không
    if (maSV.isEmpty() || hoTen.isEmpty() || txtTiengAnh.getText().isEmpty() || txtTinHoc.getText().isEmpty() || txtGDTC.getText().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Vui lòng điền đầy đủ thông tin.");
        return;
    }
    
    // Gọi phương thức ThemSinhVien từ lớp DAO.tblStudents để thêm sinh viên vào CSDL
    int addResult = DAO.tblStudents.ThemSinhVien(maSV, hoTen, diemTA, diemTH, diemGDTC);
    
    // Kiểm tra kết quả thêm sinh viên và hiển thị thông báo tương ứng
    if (addResult > 0) {
        JOptionPane.showMessageDialog(this, "Thêm sinh viên thành công.");
         HienThiDS();
        // Cập nhật hiển thị danh sách sinh viên hoặc làm bất kỳ công việc nào khác cần thiết ở đây
    } else {
        JOptionPane.showMessageDialog(this, "Thêm sinh viên thất bại. Vui lòng kiểm tra lại thông tin.");
    }
         
         
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
         int selectedRow = tblSinhVien.getSelectedRow();
    if (selectedRow >= 0) {
        String maSV = tblSinhVien.getValueAt(selectedRow, 0).toString();
        
        // Confirm deletion
        int confirm = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn xóa sinh viên này?", "Xác nhận xóa", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            int deleteResult = DAO.tblStudents.XoaSinhVien(maSV);
            
            if (deleteResult > 0) {
                JOptionPane.showMessageDialog(this, "Xóa sinh viên thành công.");
                HienThiDS(); // Refresh the display of student list
            } else if (deleteResult == -1) {
                JOptionPane.showMessageDialog(this, "Lỗi kết nối cơ sở dữ liệu.");
            } else {
                JOptionPane.showMessageDialog(this, "Xóa sinh viên thất bại. Vui lòng kiểm tra lại.");
            }
        }
    } else {
        JOptionPane.showMessageDialog(this, "Vui lòng chọn sinh viên cần xóa.");
    }
    }//GEN-LAST:event_btnDeleteActionPerformed


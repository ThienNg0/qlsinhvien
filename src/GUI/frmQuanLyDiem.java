    public void HienThiDS()
    {
      Vector<Students> ds = new Vector<Students>();
    int ketqua = DAO.tblStudents.LayDSSinhVien(ds);
    if (ketqua == -1 || ketqua == -2) {
        JOptionPane.showMessageDialog(this, "Lỗi SQL");
        return;
    } else {
        DefaultTableModel dtm = (DefaultTableModel) this.tblSinhVien.getModel();
        dtm.setRowCount(0);
        for (Students dss : ds) {
            
            dtm.addRow(new Object[]{dss.getMasv(), dss.getHoten(), dss.getTiengAnh(), dss.getTinHoc(), dss.getGdtc(), dss.tinhDiemTrungBinh()});
        }
    }
    }
    private float tinhDiemTrungBinh(float diemTA, float diemTH, float diemGDTC) {
    return (diemTA + diemTH + diemGDTC) / 3;
}
    private int currentIndex = 0;
    private void displayRecord() {
    DefaultTableModel model = (DefaultTableModel) tblSinhVien.getModel();
    if (model.getRowCount() > 0) {
        // Get the data from the current record based on the currentIndex
        String masv = model.getValueAt(currentIndex, 0).toString();
        String hoten = model.getValueAt(currentIndex, 1).toString();
        float tienganh = Float.parseFloat(model.getValueAt(currentIndex, 2).toString());
        float tinhoc = Float.parseFloat(model.getValueAt(currentIndex, 3).toString());
        float gdct = Float.parseFloat(model.getValueAt(currentIndex, 4).toString());
        float diemTB = tinhDiemTrungBinh(tienganh,tinhoc,gdct) ;
        // Display the data in the appropriate UI components
        txtMaSV.setText(masv);
        txtHoTen.setText(hoten);
        txtTiengAnh.setText(String.valueOf(tienganh));
        txtTinHoc.setText(String.valueOf(tinhoc));
        txtGDTC.setText(String.valueOf(gdct));
        lblDiemTB.setText(String.valueOf(diemTB));
    }
}

    

